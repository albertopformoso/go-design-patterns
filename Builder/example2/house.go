package main

// A House is a struct with three fields, windowType, doorType, and floor.
// @property {string} windowType - The type of window the house has.
// @property {string} doorType - The type of door the house has.
// @property {int} floor - The number of floors the house has.
type House struct {
	windowType string
	doorType   string
	floor      int
}
